<section class="encryption-info">
    <h4>Le code radio</h4>

    <p>
        Le code radio, aussi appelé alphabet phonétique, est un système utilisé pour transmettre
        des lettres de manière claire à l’oral, surtout quand la communication est mauvaise
        ou qu’il peut y avoir des confusions.
    </p>

    <p>
        Chaque lettre de l’alphabet est remplacée par un mot précis.
        Cela évite de confondre des lettres qui se ressemblent à l’oreille, comme B et D, ou M et N.
    </p>

    <ul>
        <li>
            Exemple :
            <br>
            A devient <strong>Alpha</strong>
            <br>
            B devient <strong>Bravo</strong>
            <br>
            C devient <strong>Charlie</strong>
            <br>
            D devient <strong>Delta</strong>
        </li>
        <li>
            Ainsi, un mot ou une suite de lettres peut être transmis plus clairement à distance.
        </li>
        <li>
            Par exemple, <strong>O.U.I</strong> devient :
            <br>
            <strong>Oscar Uniform India</strong>
        </li>
    </ul>

    <p>
        Ce système est très utilisé dans l’aviation, l’armée, la sécurité et les communications radio.
    </p>
</section>

